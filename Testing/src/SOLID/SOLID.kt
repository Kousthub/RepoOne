class SOLID {

    /**

    SOLID is an acronym for

    S -> Single Responsibility
    O -> Open/Closed
    L -> Liskov Substitution
    I -> Interface Segregation
    D -> Dependency Inversion

    Single Responsibility -> Each Class should change only once i.e one class should have only one responsibility
    Eg: when connection to a server one class should deal with authentication, another class should deal with sending the request, etc.


     */
